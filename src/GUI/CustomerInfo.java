    private void CustomerDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CustomerDeleteActionPerformed
        // TODO add your handling code here:
        String id = CustomerIDTextField.getText();
        int a = JOptionPane.showConfirmDialog(null, "Confirm Deletion", "Select", JOptionPane.YES_NO_OPTION);
        if (a == 0) {
            CustomerPreparedStatement.Delete(id);
            
            setVisible(false);
            new CustomerInfo().setVisible(true);
        }
    }//GEN-LAST:event_CustomerDeleteActionPerformed

    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
        DefaultTableModel dm = (DefaultTableModel) CustomerInfo.getModel();
        ArrayList<Customer> list = CustomerPreparedStatement.Display();
        for (Customer p : list) {
            dm.addRow(new Object[]{p.getCustomerID(), p.getName(), p.getPhoneNumber()});
        }
    }//GEN-LAST:event_formComponentShown

