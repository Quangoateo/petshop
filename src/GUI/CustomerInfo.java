    private void CustomerSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CustomerSearchActionPerformed
        // TODO add your handling code here:
        Customer cus = null;
        try {
            cus = CustomerPreparedStatement.Search(Integer.parseInt(CustomerIDTextField.getText()));
            if (cus == null) {
                JOptionPane.showMessageDialog(null, "No Customer with such ID!");
            } else {
                DefaultTableModel dtm = (DefaultTableModel) CustomerInfo.getModel();
                dtm.setRowCount(0);
                dtm.addRow(new Object[]{cus.getCustomerID(), cus.getName(), cus.getPhoneNumber()});
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_CustomerSearchActionPerformed

    private void CustomerCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CustomerCreateActionPerformed
        // TODO add your handling code here:
        Customer cus = new Customer();
        try {
            cus.setCustomerID(Integer.parseInt(CustomerIDTextField.getText()));
            cus.setName(CustomerNameTextField.getText());
            cus.setPhoneNumber(CustomerPhoneNumberTextField.getText());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, e);
        }
        CustomerPreparedStatement.Create(cus);
        
        setVisible(false);
        new CustomerInfo().setVisible(true);
    }//GEN-LAST:event_CustomerCreateActionPerformed

